#include "libft.h"

int	ft_atoi(const char *str)
{
	long int				n;
	unsigned int			i;
	int						sign;

	i = 0;
	n = 0;
	sign = 1;
	while (ft_iswhitespace(str[i]) == 1)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * sign);
}
